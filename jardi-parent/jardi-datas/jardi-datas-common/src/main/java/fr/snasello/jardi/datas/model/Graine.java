package fr.snasello.jardi.datas.model;

public class Graine {
	
	private String familleBotanique;
	
	private String familleLegumes;
	
	private String familleGourmande;
	
	private String type;
	
	private String nom;
	
	private String variete;

	public String getFamilleBotanique() {
		return familleBotanique;
	}

	public void setFamilleBotanique(String familleBotanique) {
		this.familleBotanique = familleBotanique;
	}

	public String getFamilleLegumes() {
		return familleLegumes;
	}

	public void setFamilleLegumes(String familleLegumes) {
		this.familleLegumes = familleLegumes;
	}

	public String getFamilleGourmande() {
		return familleGourmande;
	}

	public void setFamilleGourmande(String familleGourmande) {
		this.familleGourmande = familleGourmande;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getNom() {
		return nom;
	}

	public void setNom(String nom) {
		this.nom = nom;
	}

	public String getVariete() {
		return variete;
	}

	public void setVariete(String variete) {
		this.variete = variete;
	}
	
}
